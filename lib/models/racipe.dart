  এই স্থিতিশীলতা একটি ইমিটার রেজিস্ট্যান্স ব্যবহার করে অর্জন করা হয় যা একটি সাধারণ ইমিটার এম্প্লিফায়ারের জন্য প্রয়োজনীয় পরিমাণ স্বয়ংক্রিয় বায়াসিং প্রদান করে। এটাকে আরেকটু ব্যাখ্যা করতে নিচের বেসিক এমপ্লিফায়ার সার্কিটটি বিবেচনা করুন।


