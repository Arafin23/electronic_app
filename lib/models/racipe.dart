একটি ট্রানজিস্টর সার্কিটের জন্য প্রায়শই ব্যবহৃত বায়াসিং সার্কিটগুলির মধ্যে একটি হল ইমিটার-বায়াস সার্কিটের স্ব-বায়াসিং সহ তিনটি ট্রানজিস্টর কারেন্টের প্রাথমিক ডিসি মান সেট আপ করতে এক বা একাধিক বায়াসিং প্রতিরোধক ব্যবহার করা হয়, ( IB ), ( IC) এবং (IE)।
বাইপোলার ট্রানজিস্টর বায়াসিংয়ের দুটি সবচেয়ে সাধারণ রূপ হল: বিটা নির্ভরশীল এবং বিটা স্বাধীন। ট্রানজিস্টর বায়াস ভোল্টেজগুলি মূলত ট্রানজিস্টর বিটার উপর নির্ভরশীল, ( β ) তাই একটি ট্রানজিস্টরের জন্য বায়াসিং সেট আপ করা অন্য ট্রানজিস্টরের জন্য একই রকম নাও হতে পারে কারণ তাদের বিটা মান ভিন্ন হতে পারে। ট্রানজিস্টর বায়সিং একটি একক ফিড ব্যাক প্রতিরোধক ব্যবহার করে বা প্রয়োজনীয় বায়াসিং ভোল্টেজ প্রদানের জন্য একটি সাধারণ ভোল্টেজ ডিভাইডার নেটওয়ার্ক ব্যবহার করে অর্জন করা যেতে পারে।
নিম্নলিখিত একটি একক সরবরাহ (Vcc) থেকে ট্রানজিস্টর বেস বায়াস কনফিগারেশনের পাঁচটি উদাহরণ রয়েছে।


        একটি ট্রানজিস্টরের ফিক্সড বেস বায়াসিং

    দেখানো সার্কিটটিকে একটি "স্থির বেস বায়াস সার্কিট" বলা হয়, কারণ ট্রানজিস্টর বেস কারেন্ট, আইবি Vcc এর প্রদত্ত মানগুলির জন্য স্থির থাকে এবং সেইজন্য ট্রানজিস্টর অপারেটিং পয়েন্টকেও স্থির থাকতে হবে। এই দুটি প্রতিরোধক বায়াসিং নেটওয়ার্ক একটি নির্দিষ্ট বর্তমান বায়াস ব্যবহার করে ট্রানজিস্টরের প্রাথমিক অপারেটিং অঞ্চল স্থাপন করতে ব্যবহৃত হয়।
এই ধরনের ট্রানজিস্টর বায়াসিং বিন্যাসটিও বিটা-নির্ভর বায়াসিং কারণ অপারেশনের স্থির অবস্থা ট্রানজিস্টর বিটা β মানের একটি ফাংশন, তাই বায়াসিং পয়েন্ট একই ধরণের ট্রানজিস্টরের বৈশিষ্ট্যগুলির মতো বিস্তৃত পরিসরে পরিবর্তিত হবে। ট্রানজিস্টর ঠিক একই হবে না। ট্রানজিস্টরের ইমিটার ডায়োডটি বর্তমান লিমিটিং রেসিস্টর RB এর মাধ্যমে প্রয়োজনীয় ধনাত্মক বেস বায়াস ভোল্টেজ প্রয়োগ করে এগিয়ে পক্ষপাতী হয়। একটি আদর্শ বাইপোলার ট্রানজিস্টর ধরে নিলে, ফরোয়ার্ড বেস-ইমিটার ভোল্টেজ ড্রপ হবে 0.7V। তাহলে RB-এর মান হল সহজ: (VCC – VBE)/IB যেখানে IB কে IC/β হিসাবে সংজ্ঞায়িত করা হয়েছে।
এই একক প্রতিরোধক ধরণের বায়াসিং বিন্যাসের সাথে ট্রানজিস্টর অপারেশনের সময় বায়াসিং ভোল্টেজ এবং স্রোত স্থিতিশীল থাকে না এবং ব্যাপকভাবে পরিবর্তিত হতে পারে। এছাড়াও ট্রানজিস্টরের অপারেটিং তাপমাত্রা অপারেটিং পয়েন্টে বিরূপ প্রভাব ফেলতে পারে।    


      একটি ট্রানজিস্টরের কালেক্টর ফিডব্য়াক বায়াসিং

    এই সেলফ বায়াসিং কালেক্টর ফিডব্যাক কনফিগারেশন হল আরেকটি বিটা ডিপেন্ডেন্ট বায়াসিং পদ্ধতি যা ট্রানজিস্টরের জন্য প্রয়োজনীয় ডিসি বায়াস প্রদানের জন্য দুটি রেসিস্টরের প্রয়োজন। বেস ফিডব্যাক কনফিগারেশনের সংগ্রাহক নিশ্চিত করে যে বিটা (β) এর মান নির্বিশেষে ট্রানজিস্টর সর্বদা সক্রিয় অঞ্চলে পক্ষপাতদুষ্ট। ডিসি বেস বায়াস ভোল্টেজ সংগ্রাহক ভোল্টেজ ভিসি থেকে প্রাপ্ত হয়, এইভাবে ভাল স্থিতিশীলতা প্রদান করে।
এই সার্কিটে, বেস বায়াস রেসিস্টর, আরবি সাপ্লাই ভোল্টেজ রেল, ভিসিসির পরিবর্তে ট্রানজিস্টর কালেক্টর সি এর সাথে সংযুক্ত থাকে। এখন যদি সংগ্রাহক কারেন্ট বাড়ে, কালেক্টর ভোল্টেজ কমে যায়, বেস ড্রাইভ হ্রাস করে এবং এর ফলে স্বয়ংক্রিয়ভাবে ট্রানজিস্টর Q-পয়েন্ট স্থির রাখতে সংগ্রাহক কারেন্ট হ্রাস পায়। তাই সংগ্রাহক ফিডব্যাক বায়াসিংয়ের এই পদ্ধতিটি ট্রানজিস্টরের চারপাশে নেতিবাচক প্রতিক্রিয়া তৈরি করে কারণ আউটপুট টার্মিনাল থেকে রেসিস্টর, RB এর মাধ্যমে ইনপুট টার্মিনালে সরাসরি প্রতিক্রিয়া রয়েছে।
যেহেতু বায়াসিং ভোল্টেজটি লোড রেজিস্টর, RL জুড়ে ভোল্টেজ ড্রপ থেকে প্রাপ্ত হয়, যদি লোড কারেন্ট বাড়ে তাহলে RL জুড়ে একটি বড় ভোল্টেজ ড্রপ হবে এবং একটি অনুরূপ সংগ্রাহক ভোল্টেজ, VC হ্রাস পাবে। এই প্রভাবটি বেস কারেন্ট, আইবি-তে একটি সংশ্লিষ্ট ড্রপ ঘটাবে যা ঘুরে, আইসিকে স্বাভাবিক অবস্থায় ফিরিয়ে আনে।
বিপরীত প্রতিক্রিয়াও ঘটবে যখন ট্রানজিস্টর সংগ্রাহক কারেন্ট হ্রাস পাবে। তারপর বায়াসিংয়ের এই পদ্ধতিটিকে ট্রানজিস্টর স্থায়িত্বের সাথে সেলফ-বায়াসিং বলা হয় এই ধরনের ফিডব্যাক বায়াস নেটওয়ার্ক সাধারণত বেশিরভাগ অ্যামপ্লিফায়ার ডিজাইনের জন্য ভাল।


     ডুয়াল ফিডব্যাক ট্রানজিস্টর বায়াসিং

   পূর্ববর্তী কনফিগারেশনের বেস বায়াস নেটওয়ার্কে একটি অতিরিক্ত প্রতিরোধক যোগ করা বেটা বায়াসিং প্রতিরোধকের মধ্য দিয়ে প্রবাহিত কারেন্ট বাড়িয়ে বিটা, ( β ) এর বৈচিত্রের ক্ষেত্রে স্থিতিশীলতাকে আরও উন্নত করে।
RB1 এর মধ্য দিয়ে প্রবাহিত কারেন্ট সাধারণত সংগ্রাহক কারেন্ট, IC এর প্রায় 10% এর সমান একটি মান সেট করা হয়। স্পষ্টতই এটি বিটা, β-এর ন্যূনতম মানের জন্য প্রয়োজনীয় বেস কারেন্টের চেয়েও বেশি হতে হবে।
এই ধরনের সেলফ বায়াসিং কনফিগারেশনের একটি সুবিধা হল যে দুটি প্রতিরোধক একই সময়ে স্বয়ংক্রিয় বায়াসিং এবং Rƒ প্রতিক্রিয়া উভয়ই প্রদান করে।

    
     ইমিটার ফিডব্যাক সহ ট্রানজিস্টর বায়াসিং

  এই ধরনের ট্রানজিস্টর বায়সিং কনফিগারেশন, প্রায়ই সেলফ-ইমিটার বায়সিং বলা হয়, সংগ্রাহক কারেন্টকে আরও স্থিতিশীল করতে ইমিটার এবং বেস-সংগ্রাহক উভয় প্রতিক্রিয়া ব্যবহার করে। এর কারণ হল রেজিস্টর RB1 এবং RE পাশাপাশি ট্রানজিস্টরের বেস-ইমিটার জংশন সবই কার্যকরভাবে সাপ্লাই ভোল্টেজ, VCC এর সাথে সিরিজে যুক্ত। এই ইমিটার ফিডব্যাক কনফিগারেশনের নেতিবাচক দিক হল এটি বেস রেজিস্টর সংযোগের কারণে আউটপুট লাভ হ্রাস করে। সংগ্রাহক ভোল্টেজ ফিডব্যাক প্রতিরোধকের মধ্য দিয়ে প্রবাহিত কারেন্ট নির্ধারণ করে, RB1 উৎপন্ন করে যাকে "ডিজেনারেটিভ ফিডব্যাক" বলা হয়।
বিকিরণকারী থেকে প্রবাহিত কারেন্ট, IE (যা IC + IB-এর সংমিশ্রণ) এর ফলে RE জুড়ে এমন একটি দিকে ভোল্টেজ ড্রপ দেখা দেয় যে এটি বেস-ইমিটার জংশনকে বিপরীত পক্ষপাতিত্ব করে।
তাই যদি ইমিটার কারেন্ট বাড়ে, কালেক্টর কারেন্ট বৃদ্ধির কারণে, ভোল্টেজ ড্রপ I*REও বৃদ্ধি পায়। যেহেতু এই ভোল্টেজ রিভার্সের পোলারিটি বেস-ইমিটার জংশনকে পক্ষপাতিত্ব করে, তাই IB স্বয়ংক্রিয়ভাবে হ্রাস পায়। তাই নির্গমনকারী কারেন্ট বৃদ্ধির চেয়ে কম বৃদ্ধি পেত যদি কোন সেলফ বায়াসিং প্রতিরোধক না থাকে।
সাধারণত, রোধের মানগুলি সেট করা হয় যাতে ইমিটার রোধ RE জুড়ে ড্রপ হওয়া ভোল্টেজ VCC-এর প্রায় 10% হয় এবং রোধ RB1 এর মধ্য দিয়ে প্রবাহিত কারেন্ট সংগ্রাহক বর্তমান IC-এর 10% হয়।
সুতরাং এই ধরনের ট্রানজিস্টর বায়সিং কনফিগারেশন তুলনামূলকভাবে কম পাওয়ার সাপ্লাই ভোল্টেজে সবচেয়ে ভালো কাজ করে।


      ভোল্টেজ ডিভাইডার ট্রানজিস্টর বায়াসিং

   এখানে সাধারণ ইমিটার ট্রানজিস্টর কনফিগারেশন স্থায়িত্ব বাড়ানোর জন্য একটি ভোল্টেজ ডিভাইডার নেটওয়ার্ক ব্যবহার করে পক্ষপাতমূলক। এই বায়াসিং কনফিগারেশনের নাম এই সত্য থেকে এসেছে যে RB1 এবং RB2 দুটি প্রতিরোধক একটি ভোল্টেজ বা সম্ভাব্য বিভাজক নেটওয়ার্ক গঠন করে সরবরাহ জুড়ে তাদের কেন্দ্র বিন্দু জংশনের সাথে ট্রানজিস্টর বেস টার্মিনালকে দেখানো হয়েছে।
এই ভোল্টেজ ডিভাইডার বায়াসিং কনফিগারেশন হল সবচেয়ে বেশি ব্যবহৃত ট্রানজিস্টর বায়সিং পদ্ধতি। ট্রানজিস্টরের ইমিটার ডায়োডটি রোধ RB2 জুড়ে বিকশিত ভোল্টেজ মান দ্বারা পক্ষপাতদুষ্ট। এছাড়াও, ভোল্টেজ ডিভাইডার নেটওয়ার্ক বায়াসিং ট্রানজিস্টর সার্কিটকে বিটা পরিবর্তনের থেকে স্বাধীন করে তোলে কারণ ট্রানজিস্টর বেস, ইমিটার এবং কালেক্টর টার্মিনালগুলিতে সেট করা বায়াসিং ভোল্টেজগুলি বহিরাগত সার্কিট মানের উপর নির্ভরশীল নয়।
