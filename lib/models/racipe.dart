সাধারণত রোধ RB2 জুড়ে ভোল্টেজ ড্রপ রোধ RB1 এর তুলনায় অনেক কম। স্পষ্টতই ট্রানজিস্টর বেস ভোল্টেজ VB স্থলের সাপেক্ষে, RB2 জুড়ে ভোল্টেজের সমান হবে।
