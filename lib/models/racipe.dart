    দেখানো সাধারণ ইমিটার অ্যামপ্লিফায়ার সার্কিট ট্রানজিস্টর বেসকে পক্ষপাতিত্ব করতে একটি ভোল্টেজ ডিভাইডার নেটওয়ার্ক ব্যবহার করে এবং সাধারণ ইমিটার কনফিগারেশন বাইপোলার ট্রানজিস্টর অ্যামপ্লিফায়ার সার্কিট ডিজাইন করার একটি খুব জনপ্রিয় উপায়। এই সার্কিটের একটি গুরুত্বপূর্ণ বৈশিষ্ট্য হল ট্রানজিস্টরের গোড়ায় প্রশংসনীয় পরিমাণ কারেন্ট প্রবাহিত হয়। R1 এবং R2 দুটি বায়াসিং প্রতিরোধকের সংযোগস্থলের ভোল্টেজ, ট্রানজিস্টর বেস ভোল্টেজ, VB একটি ধ্রুবক ভোল্টেজে এবং সরবরাহ ভোল্টেজের সমানুপাতিক, Vcc ধারণ করে। মনে রাখবেন যে VB হল ভিত্তি থেকে স্থল পর্যন্ত পরিমাপ করা ভোল্টেজ, যা R2 জুড়ে প্রকৃত ভোল্টেজ ড্রপ।
  এই "ক্লাস-এ" টাইপ অ্যামপ্লিফায়ার সার্কিটটি সর্বদা এমনভাবে ডিজাইন করা হয় যাতে বেস কারেন্ট (Ib ) বায়াসিং রেসিস্টর R2 এর মধ্য দিয়ে প্রবাহিত কারেন্টের 10% এর কম হয়। সুতরাং উদাহরণস্বরূপ, যদি আমাদের 1mΑ এর একটি শান্ত সংগ্রাহক কারেন্টের প্রয়োজন হয়, তাহলে বেস কারেন্ট, IB হবে এর একশত ভাগ বা 10μΑ। তাই সম্ভাব্য বিভাজক নেটওয়ার্কের রোধ R2 এর মধ্য দিয়ে প্রবাহিত কারেন্ট অবশ্যই এই পরিমাণের কমপক্ষে 10 গুণ বা 100μΑ হতে হবে।
একটি ভোল্টেজ বিভাজক ব্যবহার করার সুবিধা এর স্থায়িত্বের মধ্যে রয়েছে। যেহেতু R1 এবং R2 দ্বারা গঠিত ভোল্টেজ বিভাজকটি হালকাভাবে লোড করা হয়েছে, তাই বেস ভোল্টেজ, Vb সহজেই গণনা করা যেতে পারে দেখানো হিসাবে সহজ ভোল্টেজ বিভাজক সূত্র ব্যবহার করে।   


       ইমিটার রেজিস্ট্যান্স স্ট্যাবিলাইজেশন

   দেখানো হিসাবে ট্রানজিস্টর ইমিটার সার্কিটে একটি একক প্রতিরোধক স্থাপন করে এমপ্লিফায়ার বায়াস ভোল্টেজকে স্থিতিশীল করা যেতে পারে। এই রোধ ইমিটার রেজিস্ট্যান্স, RE নামে পরিচিত। এই ইমিটার রেসিস্টর যোগ করার অর্থ হল ট্রানজিস্টর ইমিটার টার্মিনালটি আর গ্রাউন্ডেড বা শূন্য ভোল্ট পটেনশিয়াল নয় বরং এটির ওহমস ল সমীকরণ দ্বারা প্রদত্ত একটি ছোট পটেনশিয়ালে বসে: VE = IE x RE। কোথায়: IE হল প্রকৃত ইমিটার কারেন্ট।
এখন যদি সাপ্লাই ভোল্টেজ Vcc বৃদ্ধি পায়, তাহলে প্রদত্ত লোড রেজিস্ট্যান্সের জন্য ট্রানজিস্টর কালেক্টর কারেন্ট আইসিও বৃদ্ধি পায়। যদি সংগ্রাহক কারেন্ট বৃদ্ধি পায়, তাহলে সংশ্লিষ্ট ইমিটার কারেন্টও অবশ্যই বৃদ্ধি পাবে যার ফলে RE জুড়ে ভোল্টেজ ড্রপ বৃদ্ধি পাবে। এই ক্রিয়াটির ফলে বেস ভোল্টেজের আনুপাতিক বৃদ্ধি ঘটে কারণ VB = VE + VBE
যেহেতু বেস ভোল্টেজ বিভাজক প্রতিরোধক R1 এবং R2 দ্বারা ধ্রুবক ধরে রাখা হয়, তাই নির্গতকারী Vbe-এর সাপেক্ষে বেসের উপর DC ভোল্টেজ একটি আনুপাতিক পরিমাণে কমানো হয় এইভাবে বেস কারেন্ট ড্রাইভ হ্রাস করে এবং সংগ্রাহক কারেন্টকে আরও বাড়তে না দেয়। সরবরাহ ভোল্টেজ এবং সংগ্রাহক বর্তমান মান হ্রাস করার চেষ্টা করলে অনুরূপ ক্রিয়া ঘটে। অন্য কথায়, এই ইমিটার টার্মিনাল রেজিস্ট্যান্সের সংযোজন নেতিবাচক প্রতিক্রিয়া ব্যবহার করে ট্রানজিস্টর বেস বায়াসিং নিয়ন্ত্রণ করতে সাহায্য করে, যা বেস বায়াস ভোল্টেজের বিপরীত পরিবর্তনের সাথে সংগ্রাহক কারেন্টের যেকোনো প্রচেষ্টা পরিবর্তনকে অস্বীকার করে এবং তাই সার্কিট একটি নির্দিষ্ট স্তরে স্থিতিশীল হতে থাকে। .
এছাড়াও, যেহেতু সরবরাহের কিছু অংশ RE জুড়ে ড্রপ করা হয়, তাই এর মান যতটা সম্ভব ছোট হওয়া উচিত যাতে লোড রেজিস্ট্যান্স, RL এবং সেই কারণে আউটপুট জুড়ে সবচেয়ে বড় সম্ভাব্য ভোল্টেজ তৈরি করা যায়। যাইহোক, এর মান খুব ছোট হতে পারে না বা আবার সার্কিটের অস্থিরতা ক্ষতিগ্রস্ত হবে।
তারপরে ইমিটার রোধের মধ্য দিয়ে প্রবাহিত কারেন্টকে গণনা করা হয়:  


        ইমিটার রেজিস্টর কারেন্ট

            Ie = Ve/Re = Vb-Vbe/Re

     একটি সাধারণ নিয়ম হিসাবে, এই ইমিটার রেজিস্ট্যান্স জুড়ে ভোল্টেজ ড্রপকে সাধারণত: Vb - Vbe, বা সাপ্লাই ভোল্টেজের মানের এক-দশমাংশ (1/10তম), Vcc হিসাবে নেওয়া হয়। ইমিটার রেসিস্টর ভোল্টেজের একটি সাধারণ চিত্র হল 1 থেকে 2 ভোল্টের মধ্যে, যেটি কম। ইমিটার রেজিস্ট্যান্সের মান, Re লাভ থেকেও পাওয়া যেতে পারে কারণ এখন AC ভোল্টেজ লাভ সমান: Rl/Re       


       ইমিটার বাই-পাস ক্যাপাসিটর


   উপরের বেসিক সিরিজ ফিডব্যাক সার্কিটে, ইমিটার রেজিস্টর, RE দুটি কাজ করে: স্থিতিশীল পক্ষপাতের জন্য DC নেতিবাচক প্রতিক্রিয়া এবং সিগন্যাল ট্রান্সকন্ডাক্টেন্স এবং ভোল্টেজ লাভ স্পেসিফিকেশনের জন্য AC নেতিবাচক প্রতিক্রিয়া। কিন্তু যেহেতু ইমিটার রেজিস্ট্যান্স একটি ফিডব্যাক রেজিস্টর, তাই এসি ইনপুট সিগন্যালের কারণে ইমিটার কারেন্ট IE এর ওঠানামার কারণে এটি এমপ্লিফায়ার লাভকেও কমিয়ে দেবে। এই সমস্যাটি কাটিয়ে ওঠার জন্য একটি ক্যাপাসিটর, যাকে "ইমিটার বাইপাস ক্যাপাসিটর" বলা হয়, সিই ইমিটার রেজিস্ট্যান্স জুড়ে দেখানো হয়েছে। এই বাইপাস ক্যাপাসিটরের কারণে অ্যামপ্লিফায়ারের ফ্রিকোয়েন্সি রেসপন্স একটি নির্ধারিত কাট-অফ ফ্রিকোয়েন্সি, ƒc, বাই-পাসিং (অতএব এটির নাম) সিগন্যাল স্রোত স্থলে ভেঙ্গে যায়।
ক্যাপাসিটর হওয়ার কারণে এটি ডিসি পক্ষপাতের জন্য একটি ওপেন সার্কিট হিসাবে প্রদর্শিত হয় এবং তাই, বাইপাস ক্যাপাসিটর যোগ করার মাধ্যমে পক্ষপাতদুষ্ট স্রোত এবং ভোল্টেজগুলি প্রভাবিত হয় না। ফ্রিকোয়েন্সিগুলির পরিবর্ধক অপারেটিং পরিসরের উপর, ক্যাপাসিটারের প্রতিক্রিয়া, XC কম ফ্রিকোয়েন্সিগুলিতে অত্যন্ত উচ্চ হবে যা একটি নেতিবাচক প্রতিক্রিয়ার প্রভাব তৈরি করে, অ্যামপ্লিফায়ার লাভ হ্রাস করে।
এই বাইপাস ক্যাপাসিটর CE-এর মান সাধারণত সবচেয়ে কম কাট-অফ ফ্রিকোয়েন্সি পয়েন্টে বিকিরণকারী রোধ RE-এর মানের এক-দশমাংশ (1/10তম) ক্যাপাসিটিভ বিক্রিয়া প্রদানের জন্য বেছে নেওয়া হয়। তারপর অনুমান করা হচ্ছে যে সর্বনিম্ন সংকেত ফ্রিকোয়েন্সি 100 হবে।


      স্প্লিট ইমিটার এম্প্লিফায়ার
     
  স্প্লিট এমিট বাইপাস ক্যাপাসিটর সংযোজন করার সময়, সিই বিটা, ( β ) এর অনিশ্চয়তার প্রভাব মোকাবেলা করে পরিবর্ধক লাভ নিয়ন্ত্রণ করতে সাহায্য করে, ( β ), এর প্রধান অসুবিধাগুলির মধ্যে একটি হল উচ্চ ফ্রিকোয়েন্সিতে ক্যাপাসিটর বিক্রিয়া এত কম হয়ে যায় যে এটি কার্যকরভাবে শর্ট করে। বিকিরণ প্রতিরোধের আউট, ফ্রিকোয়েন্সি বৃদ্ধি হিসাবে RE.
ফলাফল হল যে উচ্চ ফ্রিকোয়েন্সিতে ক্যাপাসিটরের বিক্রিয়া খুব কম AC ফিডব্যাক নিয়ন্ত্রণের অনুমতি দেয় কারণ RE কমিয়ে দেওয়া হয় যার অর্থ হল ট্রানজিস্টরের AC ভোল্টেজ লাভ ব্যাপকভাবে বৃদ্ধি পায় যা পরিবর্ধককে স্যাচুরেশনে নিয়ে যায়।
সম্পূর্ণ অপারেটিং ফ্রিকোয়েন্সি রেঞ্জের উপর অ্যামপ্লিফায়ার লাভ নিয়ন্ত্রণ করার একটি সহজ উপায় হল বিকিরণ প্রতিরোধকে দুটি অংশে বিভক্ত করা যেমন দেখানো হয়েছে।


      স্প্লিট ইমিটার রেজিস্টর

    ইমিটার লেগের রোধকে দুটি ভাগে বিভক্ত করা হয়েছে: RE1 এবং RE2 নিম্ন রোধ জুড়ে সমান্তরালভাবে সংযুক্ত বাই-পাস ক্যাপাসিটর সহ ইমিটার পায়ের মধ্যে একটি ভোল্টেজ বিভাজক নেটওয়ার্ক তৈরি করে।
উপরের রোধ, RE1 আগের মতই একই মান কিন্তু ক্যাপাসিটর দ্বারা বাইপাস করা হয় না তাই সিগন্যাল প্যারামিটার গণনা করার সময় অবশ্যই বিবেচনা করা উচিত। নিম্ন রোধ RE2 ক্যাপাসিটরের সাথে সমান্তরালভাবে সংযুক্ত থাকে এবং সিগন্যাল প্যারামিটার গণনা করার সময় এটিকে শূন্য ওহম হিসাবে বিবেচনা করা হয় কারণ এটি উচ্চ ফ্রিকোয়েন্সিতে ছোট হয়ে যায়।
এখানে সুবিধা হল যে আমরা ইনপুট ফ্রিকোয়েন্সিগুলির সম্পূর্ণ পরিসরের উপর পরিবর্ধকের AC লাভ নিয়ন্ত্রণ করতে পারি। DC-তে ইমিটার রেজিস্ট্যান্সের মোট মান RE1 + RE2 এর সমান যখন উচ্চতর AC ফ্রিকোয়েন্সিতে ইমিটার রেজিস্ট্যান্স ঠিক থাকে: RE1, উপরের মূল আনবাইপাসড সার্কিটে যেমন ছিল।
তাহলে রোধের মান কি, RE2। ভাল এটি নির্ভর করবে নিম্ন ফ্রিকোয়েন্সি কাট-অফ পয়েন্টে প্রয়োজনীয় ডিসি ভোল্টেজ লাভের উপর। আমরা আগে বলেছিলাম যে উপরের সার্কিটের লাভ সমান ছিল: RL/RE যা উপরের আমাদের সাধারণ ইমিটার সার্কিটের জন্য 10 (1kΩ/100Ω) গণনা করা হয়েছিল। কিন্তু এখন DC-তে লাভ হবে সমান: RL / (RE1 + RE2)


    ইমিটার রেজিস্ট্যান্স সারাংশ

  তারপরে সংক্ষেপে বলতে গেলে, একটি ট্রানজিস্টরের বর্তমান পরিবর্ধন প্যারামিটার, β উত্পাদন সহনশীলতার কারণে এবং সরবরাহ ভোল্টেজ এবং অপারেটিং তাপমাত্রার তারতম্যের কারণে একই ধরণের এবং অংশ সংখ্যার একটি ডিভাইস থেকে অন্য ডিভাইসে উল্লেখযোগ্যভাবে পরিবর্তিত হতে পারে।
তারপর একটি সাধারণ ইমিটার ক্লাস-এ এমপ্লিফায়ার সার্কিটের জন্য একটি বায়াসিং সার্কিট ব্যবহার করা প্রয়োজন যা অপারেটিং Q-পয়েন্টকে স্থিতিশীল করবে যা ডিসি কালেক্টর কারেন্ট, আইসি বিটা থেকে স্বাধীন। স্থিতিশীলতা প্রদানের জন্য বিকিরণকারী পায়ে একটি ইমিটার রেজিস্ট্যান্স, RE যোগ করে ইমিটার কারেন্টের মানের উপর β-এর প্রভাব হ্রাস করা যেতে পারে।
এই ইমিটার রেজিস্ট্যান্স জুড়ে ভোল্টেজ ড্রপ সাধারণত 1 থেকে 2 ভোল্টের মধ্যে দেওয়া হয়। ইমিটার রোধকে একটি উপযুক্ত বাইপাস ক্যাপাসিটর দ্বারা সম্পূর্ণরূপে বাইপাস করা যেতে পারে, সিই উচ্চতর এসি লাভ অর্জনের জন্য ইমিটার রোধের সাথে সমান্তরালভাবে সংযুক্ত বা আংশিকভাবে বাইপাস করে, একটি স্প্লিট-ইমিটার ভোল্টেজ বিভাজক নেটওয়ার্ক ব্যবহার করে যা DC লাভ এবং বিকৃতি হ্রাস করে। এই ক্যাপাসিটরের মান সর্বনিম্ন সংকেত ফ্রিকোয়েন্সিতে এর ক্যাপাসিটিভ রিঅ্যাক্ট্যান্স (XC) মান থেকে নির্ধারিত হয়।


   ক্লাস এ এমপ্লিফায়ার

    উচ্চ রৈখিকতা এবং লাভ অর্জনের জন্য, একটি ক্লাস A পরিবর্ধকের আউটপুট পর্যায়টি সর্বদা "চালু" (পরিচালনা) পক্ষপাতমূলক থাকে। তারপরে একটি পরিবর্ধককে "ক্লাস A" হিসাবে শ্রেণীবদ্ধ করার জন্য আউটপুট পর্যায়ে শূন্য সংকেত নিষ্ক্রিয় কারেন্ট অবশ্যই সর্বাধিক লোড কারেন্ট (সাধারণত একটি লাউডস্পীকার) বৃহত্তম আউটপুট সংকেত তৈরি করতে প্রয়োজনীয় সমান বা তার চেয়ে বেশি হতে হবে।
একটি ক্লাস A পরিবর্ধক তার বৈশিষ্ট্যযুক্ত বক্ররেখার রৈখিক অংশে কাজ করে, একক আউটপুট ডিভাইসটি আউটপুট তরঙ্গরূপের সম্পূর্ণ 360 ডিগ্রির মাধ্যমে সঞ্চালিত হয়। তারপর ক্লাস A পরিবর্ধক একটি বর্তমান উৎসের সমতুল্য।
যেহেতু একটি শ্রেণী A পরিবর্ধক রৈখিক অঞ্চলে কাজ করে, তাই ট্রানজিস্টর বেস (বা গেট) ডিসি বায়সিং ভোল্টেজ সঠিক অপারেশন এবং কম বিকৃতি নিশ্চিত করার জন্য সঠিকভাবে বেছে নেওয়া উচিত। যাইহোক, যেহেতু আউটপুট ডিভাইসটি সর্বদা "চালু" থাকে, এটি ক্রমাগত কারেন্ট বহন করে, যা অ্যামপ্লিফায়ারে ক্রমাগত শক্তি হ্রাসকে প্রতিনিধিত্ব করে।
এই ক্রমাগত পাওয়ার ক্লাসের ক্ষতির কারণে A অ্যামপ্লিফায়ারগুলি প্রচুর পরিমাণে তাপ তৈরি করে যা তাদের খুব কম কার্যক্ষমতা প্রায় 30% এ যোগ করে, যা উচ্চ-শক্তি পরিবর্ধনের জন্য তাদের অবাস্তব করে তোলে। এছাড়াও অ্যামপ্লিফায়ারের উচ্চ নিষ্ক্রিয় কারেন্টের কারণে, পাওয়ার সাপ্লাইকে অবশ্যই সেই অনুযায়ী মাপ করা উচিত এবং কোনও অ্যামপ্লিফায়ার হুম এবং শব্দ এড়াতে ভালভাবে ফিল্টার করা উচিত। অতএব, ক্লাস A পরিবর্ধকগুলির কম দক্ষতা এবং অতিরিক্ত গরম করার সমস্যার কারণে, আরও দক্ষ পরিবর্ধক ক্লাস তৈরি করা হয়েছে।


  ক্লাস বি এমপ্লিফায়ার

   
   ক্লাস B পরিবর্ধক পূর্ববর্তী শ্রেণীর A পরিবর্ধকের সাথে সম্পর্কিত দক্ষতা এবং গরম করার সমস্যার সমাধান হিসাবে উদ্ভাবিত হয়েছিল। বেসিক ক্লাস B অ্যামপ্লিফায়ার দুটি কমপ্লিমেন্টারি ট্রানজিস্টর ব্যবহার করে ওয়েভফর্মের প্রতিটি অর্ধেকের জন্য FET-এর বাইপোলার যার আউটপুট স্টেজ একটি "পুশ-পুল" টাইপ বিন্যাসে কনফিগার করা হয়েছে, যাতে প্রতিটি ট্রানজিস্টর ডিভাইস আউটপুট তরঙ্গরূপের মাত্র অর্ধেককে প্রশস্ত করে।
ক্লাস B এম্প্লিফায়ারে, কোন DC বেস বায়াস কারেন্ট নেই কারণ এর শান্ত কারেন্ট শূন্য, যাতে dc শক্তি ছোট এবং তাই এর কার্যকারিতা A ক্লাস এম্প্লিফায়ারের তুলনায় অনেক বেশি। যাইহোক, দক্ষতার উন্নতির জন্য প্রদত্ত মূল্যটি সুইচিং ডিভাইসের রৈখিকতার মধ্যে রয়েছে। নেতিবাচক ট্রানজিস্টর "বন্ধ" সুইচ করা হয়. একইভাবে, যখন ইনপুট সিগন্যাল নেগেটিভ হয়ে যায়, তখন ইতিবাচক ট্রানজিস্টর "অফ" করে যখন নেতিবাচক পক্ষপাতদুষ্ট ট্রানজিস্টর "অন" করে এবং সিগন্যালের নেতিবাচক অংশ পরিচালনা করে। এইভাবে ট্রানজিস্টর ইনপুট সিগন্যালের ইতিবাচক বা নেতিবাচক অর্ধচক্রে মাত্র অর্ধেক সময় পরিচালনা করে।
তারপরে আমরা দেখতে পাব যে ক্লাস B পরিবর্ধকের প্রতিটি ট্রানজিস্টর ডিভাইস কঠোর সময়ের পরিবর্তনে আউটপুট তরঙ্গরূপের মাত্র এক অর্ধেক বা 180 ডিগ্রির মধ্য দিয়ে সঞ্চালিত হয়, কিন্তু আউটপুট পর্যায়ে সিগন্যাল তরঙ্গরূপের উভয় অর্ধাংশের জন্য ডিভাইস রয়েছে বলে দুটি অর্ধেক একত্রিত হয়। সম্পূর্ণ লিনিয়ার আউটপুট তরঙ্গরূপ উত্পাদন করতে।
অ্যামপ্লিফায়ারের এই পুশ-পুল ডিজাইন স্পষ্টতই ক্লাস A-এর তুলনায় প্রায় 50% বেশি দক্ষ, কিন্তু ক্লাস B অ্যামপ্লিফায়ার ডিজাইনের সমস্যা হল যে এটি ট্রানজিস্টর ডেড ব্যান্ডের কারণে তরঙ্গরূপের জিরো-ক্রসিং পয়েন্টে বিকৃতি তৈরি করতে পারে। ইনপুট বেস ভোল্টেজ -0.7V থেকে +0.7 পর্যন্ত।
আমরা ট্রানজিস্টর টিউটোরিয়াল থেকে মনে রাখি যে একটি বাইপোলার ট্রানজিস্টর পরিচালনা শুরু করতে এটি প্রায় 0.7 ভোল্টের একটি বেস-ইমিটার ভোল্টেজ নেয়। তারপর একটি ক্লাস B পরিবর্ধক, আউটপুট ট্রানজিস্টর এই ভোল্টেজ অতিক্রম না হওয়া পর্যন্ত অপারেশনের একটি "অন" অবস্থায় "পক্ষপাতমূলক" হয় না।
এর মানে হল যে তরঙ্গরূপের যে অংশটি এই 0.7 ভোল্টের উইন্ডোর মধ্যে পড়ে তা সঠিকভাবে পুনরুত্পাদন করা হবে না যার ফলে ক্লাস B পরিবর্ধক নির্ভুল অডিও অ্যামপ্লিফায়ার অ্যাপ্লিকেশনের জন্য অনুপযুক্ত হবে।
এই শূন্য-ক্রসিং বিকৃতিকে কাটিয়ে উঠতে (এছাড়াও ক্রসওভার ডিস্টরশন নামেও পরিচিত) ক্লাস এবি অ্যামপ্লিফায়ার তৈরি করা হয়েছিল।


  ক্লাস এবি এমপ্লিফায়ার
   
   এর নাম অনুসারে, ক্লাস এবি অ্যামপ্লিফায়ার হল "ক্লাস এ" এবং "ক্লাস বি" টাইপ অ্যামপ্লিফায়ারগুলির সংমিশ্রণ যা আমরা উপরে দেখেছি। অ্যামপ্লিফায়ারের AB শ্রেণীবিভাগ বর্তমানে অডিও পাওয়ার অ্যামপ্লিফায়ার ডিজাইনের সবচেয়ে সাধারণ ব্যবহৃত ধরনের একটি। ক্লাস AB পরিবর্ধক হল উপরে বর্ণিত একটি ক্লাস B পরিবর্ধকের একটি বৈচিত্র, ব্যতীত উভয় ডিভাইসকে একই সময়ে তরঙ্গরূপ ক্রসওভার পয়েন্টের চারপাশে পরিচালনা করার অনুমতি দেওয়া হয় যা পূর্ববর্তী ক্লাস B পরিবর্ধকের ক্রসওভার বিকৃতির সমস্যা দূর করে।
দুটি ট্রানজিস্টরের একটি খুব ছোট বায়াস ভোল্টেজ রয়েছে, সাধারণত 5 থেকে 10% শান্ত স্রোত ট্রানজিস্টরকে তার কাট-অফ পয়েন্টের ঠিক উপরে পক্ষপাতিত্ব করার জন্য। তারপর কন্ডাক্টিং ডিভাইস, হয় FET-এর বাইপোলার, এক অর্ধেক চক্রের জন্য "চালু" থাকবে, কিন্তু ইনপুট সিগন্যালের একটি পূর্ণ চক্রের চেয়ে অনেক কম। অতএব, একটি শ্রেণী AB পরিবর্ধক ডিজাইনে প্রতিটি পুশ-পুল ট্রানজিস্টর B শ্রেণীতে পরিবাহনের অর্ধচক্রের থেকে সামান্য বেশি সময় পরিবাহিত হয়, কিন্তু A শ্রেণীর পরিবাহনের সম্পূর্ণ চক্রের তুলনায় অনেক কম।
অন্য কথায়, একটি শ্রেণী AB পরিবর্ধক এর পরিবাহী কোণ 180o এবং 360o এর মধ্যে রয়েছে যা দেখানো হয়েছে নির্বাচিত বায়াস পয়েন্টের উপর নির্ভর করে। সিরিজ ডায়োড বা প্রতিরোধক দ্বারা প্রদত্ত এই ছোট পক্ষপাত ভোল্টেজের সুবিধা হল যে ক্লাস B পরিবর্ধক বৈশিষ্ট্য দ্বারা সৃষ্ট ক্রসওভার বিকৃতিটি কাটিয়ে উঠতে পারে, ক্লাস A পরিবর্ধক ডিজাইনের অদক্ষতা ছাড়াই। সুতরাং শ্রেণী AB পরিবর্ধকটি দক্ষতা এবং রৈখিকতার পরিপ্রেক্ষিতে ক্লাস A এবং ক্লাস B এর মধ্যে একটি ভাল সমঝোতা, যার রূপান্তর দক্ষতা প্রায় 50% থেকে 60% পর্যন্ত পৌঁছেছে।


  ক্লাস সি এমপ্লিফায়ার

  ক্লাস সি অ্যামপ্লিফায়ার ডিজাইনের সবচেয়ে বেশি দক্ষতা রয়েছে কিন্তু এখানে উল্লেখ করা অ্যামপ্লিফায়ারগুলির ক্লাসগুলির মধ্যে সবচেয়ে দুর্বল রৈখিকতা। পূর্ববর্তী শ্রেণী, A, B এবং AB রৈখিক পরিবর্ধক হিসাবে বিবেচিত হয়, কারণ আউটপুট সংকেত প্রশস্ততা এবং ফেজ রৈখিকভাবে ইনপুট সংকেত প্রশস্ততা এবং পর্বের সাথে সম্পর্কিত।
যাইহোক, ক্লাস C পরিবর্ধকটি অত্যন্ত পক্ষপাতদুষ্ট যাতে একটি ইনপুট সাইনোসয়েডাল সিগন্যাল চক্রের অর্ধেকেরও বেশি সময় আউটপুট কারেন্ট শূন্য থাকে যার কাট-অফ পয়েন্টে ট্রানজিস্টর অলস থাকে। অন্য কথায়, ট্রানজিস্টরের জন্য পরিবাহী কোণ উল্লেখযোগ্যভাবে 180 ডিগ্রির চেয়ে কম, এবং সাধারণত 90 ডিগ্রি এলাকার কাছাকাছি।
যদিও ট্রানজিস্টর বায়াসিংয়ের এই ফর্মটি এমপ্লিফায়ারকে প্রায় 80% এর অনেক উন্নত দক্ষতা দেয়, এটি আউটপুট সিগন্যালের একটি খুব ভারী বিকৃতি প্রবর্তন করে। অতএব, ক্লাস সি পরিবর্ধক অডিও পরিবর্ধক হিসাবে ব্যবহারের জন্য উপযুক্ত নয়। এর ভারী অডিও বিকৃতির কারণে, ক্লাস সি পরিবর্ধকগুলি সাধারণত উচ্চ ফ্রিকোয়েন্সি সাইন ওয়েভ অসিলেটর এবং নির্দিষ্ট ধরণের রেডিও ফ্রিকোয়েন্সি অ্যামপ্লিফায়ারগুলিতে ব্যবহৃত হয়, যেখানে অ্যামপ্লিফায়ার আউটপুটে উত্পাদিত কারেন্টের স্পন্দনগুলি একটি নির্দিষ্ট ফ্রিকোয়েন্সির সম্পূর্ণ সাইন তরঙ্গে রূপান্তরিত হতে পারে। এর সংগ্রাহক সার্কিটে এলসি রেজোন্যান্ট সার্কিট ব্যবহার।


  এমপ্লিফায়ার ক্লাস সারাংশ

তারপরে আমরা দেখেছি যে একটি অ্যামপ্লিফায়ারের শান্ত ডিসি অপারেটিং পয়েন্ট (কিউ-পয়েন্ট) পরিবর্ধক শ্রেণিবিন্যাস নির্ধারণ করে। পরিবর্ধক বৈশিষ্ট্য বক্ররেখার লোড লাইনের অর্ধেক পথে Q-পয়েন্টের অবস্থান নির্ধারণ করে, পরিবর্ধকটি একটি শ্রেণি 
A পরিবর্ধক হিসাবে কাজ করবে। Q-পয়েন্টকে নিচের দিকে সরানোর মাধ্যমে লোড লাইনটি পরিবর্ধককে AB, B বা C শ্রেণীতে পরিবর্তিত করে। অডিও অ্যামপ্লিফায়ারগুলির পাশাপাশি অ্যামপ্লিফায়ার ডিজাইনের সুইচিং সম্পর্কিত অনেকগুলি উচ্চ দক্ষতার অ্যামপ্লিফায়ার ক্লাস রয়েছে যা পাওয়ার লস কমাতে এবং দক্ষতা বাড়াতে বিভিন্ন সুইচিং কৌশল ব্যবহার করে। নীচে তালিকাভুক্ত কিছু পরিবর্ধক শ্রেণির নকশাগুলি বিদ্যুতের ক্ষতি কমাতে RLC অনুরণনকারী বা একাধিক পাওয়ার-সাপ্লাই ভোল্টেজ ব্যবহার করে, অথবা ডিজিটাল ডিএসপি (ডিজিটাল সিগন্যাল প্রসেসিং) টাইপ এমপ্লিফায়ার যা পালস প্রস্থ মড্যুলেশন (PWM) স্যুইচিং কৌশল ব্যবহার করে।


অন্যান্য সাধারণ এমপ্লিফায়ার ক্লাস

১)ক্লাস ডি অ্যামপ্লিফায়ার - একটি ক্লাস ডি অডিও অ্যামপ্লিফায়ার মূলত একটি নন-লিনিয়ার সুইচিং অ্যামপ্লিফায়ার বা PWM অ্যামপ্লিফায়ার। ক্লাস-ডি অ্যামপ্লিফায়ারগুলি তাত্ত্বিকভাবে 100% দক্ষতায় পৌঁছতে পারে, কারণ একটি চক্রের সময় ভোল্টেজ এবং কারেন্ট ওয়েভফর্ম ওভারল্যাপ ছিল না কারণ কারেন্ট শুধুমাত্র চালু থাকা ট্রানজিস্টরের মাধ্যমে আঁকা হয়।

২)ক্লাস এফ পরিবর্ধক - ক্লাস-এফ পরিবর্ধক আউটপুট তরঙ্গরূপকে একটি বর্গাকার তরঙ্গে আকৃতি দেওয়ার জন্য আউটপুট নেটওয়ার্কে হারমোনিক অনুরণনকারী ব্যবহার করে দক্ষতা এবং আউটপুট উভয়ই বৃদ্ধি করে। যদি অসীম হারমোনিক টিউনিং ব্যবহার করা হয় তবে ক্লাস-এফ পরিবর্ধকগুলি 90% এর বেশি উচ্চ দক্ষতায় সক্ষম।

৩)ক্লাস জি অ্যামপ্লিফায়ার - ক্লাস জি বেসিক ক্লাস AB অ্যামপ্লিফায়ার ডিজাইনে উন্নতির প্রস্তাব দেয়। ক্লাস জি বিভিন্ন ভোল্টেজের একাধিক পাওয়ার সাপ্লাই রেল ব্যবহার করে এবং ইনপুট সিগন্যাল পরিবর্তনের সাথে সাথে এই সরবরাহ রেলগুলির মধ্যে স্বয়ংক্রিয়ভাবে স্যুইচ করে। এই ধ্রুবক স্যুইচিং গড় বিদ্যুত খরচ হ্রাস করে, এবং সেইজন্য নষ্ট তাপের কারণে শক্তি হ্রাস পায়।

